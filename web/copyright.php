weitergegeben werden. Man bezeichnet dies auch als Copyleft.</p>
<p>Obwohl ich als Autor die Möglichkeit gehabt hätte, das Programm unter einem normalen Copyright herauszugeben (d.h. dessen Verwendung und Weitergabe einzuschränken und
für die Benutzung Lizenzgebühren zu verlangen), tue ich dies aus einer Grundüberzeugung heraus nicht:</p>
<ul>
<li>Zum einen bin ich der Meinung, dass nur Freie Software eine freie Gesellschaft ermöglicht.</li> 
<li>Zum anderen hätte ein Copyright die Weiterentwicklung des Programmes von vornherein eingeschränkt.</li>
</ul>

<p>Als Beispiel: Wenn jemand - basierend auf VSTENO - ein System für die DEK (Deutsche Einheits Kurzschrift) umsetzen möchte, so ist dies uneingeschränkt möglich.</p>
<h2>Gratis?</h2>
<p>Eine immer wieder diskutierte Frage ist die, ob Freie Software gratis sei? Meinerseits möchte ich hierzu als Autor freier Software klar Stellung beziehen:</p>
<ul>
<li><p>Als erstes gilt: Nichts auf dieser Welt ist gratis! Für alles muss eine Leistung erbracht werden, das ist wohl ein physikalischer (thermodynamischer) Grundsatz - und im übrigen der Grund,
warum es keine "Perpetuum Mobile" gibt. Freie Software kommt also nicht von nichts: Dahinter steckt immer Arbeit!</p></li>
<li><p>Als nächstes gilt: Anhänger Freier Software sind oft Idealisten. Sie glauben - entgegen jeder Vernunft - an eine bessere Welt. Insofern sind sie tatsächlich bereit, auf das klassische "do,
ut des" zu pfeifen, und einfach mal Zeit, Energie und zum Teil auch Geld in etwas zu investieren, wovon sie nicht wissen, ob sie jemals etwas "zurückerhalten" werden. Viele Autoren gehen
tatsächlich soweit, dass sie ihre Programme als grundsätzlich gratis deklarieren, verbunden mit der Bitte um eine Spende (was etwas widersprüchlich scheint).</p></li>
<li><p>drittens füge ich persönlich an: Ich war nie ein Fan der Idee, dass Freie Software gratis sein soll. Als Student, das ist wahr, habe ich für freie Software praktisch nie bezahlt (war damals
auch eine Frage des Budgets). Heute ist es für mich keine Frage, dass ich für freie Software, die ich verwende, auch bezahle. Ich sehe das so: Würde ich kommerzielle (also nicht freie) Programme
verwenden, müsste ich diese kaufen (also Lizenzgebühren entrichten). Warum soll ich also für Freie Software nicht bezahlen - zumal ich dies aus völlig freien Stücken tue und in diesem Fall von
viel mehr Freiheiten profitiere, als ich es bei kommerzieller Software wäre?</p></li>
<li><p>als letztes lassen Sie mich noch anfügen: Mit "viel mehr Freiheiten" ist zum Beispiel gemeint, dass sie Freie Software eben nicht einschränkt (sie dürfen das Programm zum Beispiel auf so
Geräten verwenden wie Sie möchten), dass sie mitunter ihre "Lizenzgebühr" frei wählen können (z.B. mehr oder weniger spenden, je nachdem, ob sie das Programm viel oder wenig verwenden oder 
ein Projekt mehr oder weniger unterstützen möchten).</p></li>
</ul>
</p>
<p><b>Fazit:</b> Dadurch, dass ich das Programm unter die Freie-Software-Lizenz stelle, gebe ich Ihnen alle erdenklichen Freiheiten. Inklusive der Freiheit, das Programm GRATIS zu verwenden - und
das sage ich ohne Ironie: Wenn Sie Student, ohne Geld oder in einer Situation sind, in der Sie keine Spende tätigen können, oder möchten, dann verwenden Sie dieses Programm bitte gratis (und 
ohne sich zu einer Spende verpflichtet zu fühlen).</p>
<p>Wenn Sie aber über die finanziellen Mittel verfügen, dann bitte ich Sie, sich kurz innezuhalten und zu überlegen, was Freie Software für unsere Gesellschaft
bedeuten kann: Keine Copyright-Hölle, in der irgendwelche Konzerne sie überwachen, um herauszufinden, ob sie allenfalls "illegal" kostenpflichtige Produkte herunterladen, sondern freien Zugang
zu allen Inhalten, in der sie selber entscheiden, ob und wofür sie bezahlen (und welche Konzerne sie z.B. unterstützen und welche nicht).</p>
<p>Das wäre das Schöne daran: eine Freie Gesellschaft. 
Die Kehrseite davon ist, dass eine solche Freiheit auch mit Verantwortung einhergeht: Niemand zwingt Sie dazu, "Urhebern" für ihre Arbeit etwas zu bezahlen. Dies ist im übrigen genau die
Situation, die wir heute im Internet weit gehend vorfinden: Die meisten Dienste sind (gratis) und die "Konsument/innen" gehen inzwischen auch davon aus (weil sie sich daran gewöhnt haben), dass 
alles gratis ist.<p>
</p>Aber ich bestehe noch einmal darauf: Nichts ist gratis! Wenn es nichts "kostet", dann bezahlen Sie anders für das Produkt (z.B. indem Sie endlos Werbung ertragen oder Konzerne
ihre Daten zu Geld machen). Deshalb noch einmal: Wenn Sie dies nicht wollen, dann treffen Sie bitte die richtige Entscheidung und spenden Sie an Freie-Software-Projekte. Es muss nicht an mich bzw.
an VSTENO sein: Ich habe selber jahrelang von Freier Software und der Arbeit anderer profitiert, deshalb ist es für mich auch in Ordnung, wenn Sie an andere Projekte (wie z.B. die 
<a href="https://www.fsf.org/">Free Software Foundation</a> oder <a href="https://trisquel.info">Trisquel</a> - die Linux-Distribution, auf welcher ich VSTENO entwickelt habe - spenden).<p>
</p>Meine Bitte ist somit nur die:<br>Tun Sie einen Schritt Richtung "freiwillige und faire Bezahlung der Urheber/innen" - es kommt uns allen zugute!
</p>
