<h1>VSTENO feiert 3 Jahre!</h1>

<p>Bereits drei Jahre sind seit dem ersten Spatenstich zu VSTENO vergangen. Während es in den ersten zwei Jahren vor allem darum ging, 
das Kernprogramm als solches zu entwickeln, war das dritte Jahr voll und ganz der Stenografie gewidmet.</p>
<p>Konkret sollten zwei Hauptziele verwirklicht werden:</p> 

<ul>
<li>(1) Verfeinerung des deutschsprachigen Modells </li>
<li>(2) Herausgabe gedruckter Taschenbücher </li>
</ul>

<p>Zusätzlich sollte darauf hingearbeitet werden, das Projekt mittelfristig finanziell selbsttragend zu machen, sprich: Mit dem Verkauf von 
Büchern ausreichend Einnahmen zu generieren, um mindestens die laufenden Kosten decken.</p>
<p>Die ersten zwei Ziele konnten voll und ganz verwirklicht werden. Finanziell hingegen schliesst das Jahr mit einem Defizit, welches 
allerdings dadurch bedingt ist, dass die publizierten Bücher vorfinanziert werden mussten.</p>
<p>Im Folgenden wiederum weitere Details zu den verschiedenen Bereichen.</p>

<h2>Deutschsprachiges Model</h2>
<p>Die von VSTENO angewandten Regeln konnten so weit verfeinert werden, dass ein spürbarer Gewinn an Lesevergnügen resultiert. Im 
Vergleich zu vor einem Jahr, wo die Fehlerquote noch bei 1-2 Fehlern pro Seite lag, können nun oft mehrere Seiten gelesen werden, ohne 
auf einen Fehler zu stossen. Auch das Schriftbild hat sich merklich verbessert.</p>
<p>Ausnahmen bestätigen natürlich nach wie vor die Regel: Stenografie ist und bleibt für einen Computer eine schwierige Disziplin - 
und voraussichtlich wird es hier nie möglich sein, 100% zu erreichen.</p>

<h2>Gedruckte Bücher</h2>
<p>Mit <a href='dwz_details.php'><i>Der Widerspenstigen Zähmung</i></a> und <a href='matto_regiert_details.php'><i>Matto regiert</i></a> 
erschienen erstmals zwei längere Taschenbücher, welche vollständig automatisiert übertragen wurden. Natürlich war vor der "automatisierten" 
Übertragungen einiges an manueller Arbeit nötig (Anpassung der Modelle wie oben erwähnt). Das Gute an VSTENO ist jedoch, dass diese 
Arbeit im Anschluss allen zugute kommt, da weitere Bücher dann automatisch mit weniger Fehlern generiert werden.</p>

Die Bücher wurden auf verschiedenen Kanälen (Internet und Verbandszeitschriften) und in verschiedener Form beworben. Sowohl die Nachfrage 
als auch die Feedbacks darauf waren erfreulich. Vor allem zeigt sich ein klarer qualitativer und quantitativer Unterschied in den 
Reaktionen: Ein gedrucktes Buch ist ein haptisches Erlebnis, welches mehr und persönlichere Reaktion auf das Projekt hervorruft - was sehr 
erfreulich ist!</p>

<h2>Finanzielles</h2>
<p>Wie bereits unter <a href='donation.php'>Spenden</a> und <a href='license_fees.php'>Lizenzgebühren</a> vermerkt, 
gehe ich im 3. Jahr dazu über Spenden und Buchverkäufe in den gleichen Topf zu werfen, um damit (1) Fixkosten zu decken und 
(2) mit einem allfälligen Gewinn auch Lizenzgebühren abzugelten.</p> 
<p>Die erste Abrechnung nach dieser Regelung präsentiert sich nun wie folgt:</p>
<ul>
    <li>Von beiden Büchern wurden je 50 Stück als erste Auflage gedruckt (vorfinanziert)</li>
    <li>Ein Teil wurde als Ansichtsexemplare verschickt, die restlichen werden kontinuierlich verkauft</li>
    <li>Im Moment sind noch an Lager: 7 Stück der "Widerspenstigen", 33 Stück von "Matto"</li>
</ul>
<p>Ebenfalls fallen bei den gedruckten Büchern Kosten für Vordrucke und Versand an. Auf der Serverseite wiederum fallen 
die bereits erwähnten kosten für das Hosting und die Internet-Adresse an. Summa summarum ergibt sich hier:
<ul>
<li>Aufwand: 1559.50 CHF</li>
<li>Einnahmen: 638.- CHF</li>
<li>Total: <b style="color:red">-921.50 CHF</b></li>
</ul>
<p>Dieser buchhalterische Verlust, welcher auf das kommende Jahr übertragen wird, sollte sich mit dem Verkauf der vorfinanzierten Bücher 
ausgleichen. Ich hoffe somit, in einem Jahr erstmals Lizenzgebühren ans <a href='http://www.gutenberg.org'>Projekt Gutenberg</a> 
(e-Text der "Widerspenstigen") auszahlen zu können.</p>

