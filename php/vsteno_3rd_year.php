<ul>
<li>Vol de Nuit (Antoine de St-Exupéry): Obwohl die Copyright-Geschichte von Saint-Exupérys Werken 
<a href='https://www.liberation.fr/culture/2015/06/03/pourquoi-saint-exupery-est-il-tombe-dans-le-domaine-public-partout-sauf-en-france_1322085/'>kompliziert</a> 
