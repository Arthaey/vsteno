<p>Ansonsten wird es in etwa weitergehen wie bis jetzt, d.h. VSTENO wird kontinuierlich weiterentwickelt werden. Wie im 3. Jahr wird der 
Fokus dabei nicht auf der Programmentwicklung liegen (die Funktionalitäten sind inzwischen ausreichend), sondern auf den stenografischen 
Modellen. Dies alles wird auch davon abhängen, wie viel Zeit ich in Zukunft in das Projekt investieren kann. Als Grössenordnung: Nach 3 
Jahren stecken in VSTENO rund 1860 Stunden Arbeit (Ferien abgerechnet also etwa ein Jahr Vollzeit:). Ich bin nach wie vor begeistert von 
der Stenografie und dem Programm - dennoch zieht es mich programmiertechnisch auch wieder in andere Richtungen (Assembler:) und somit 
werde ich den Aufwand für VSTENO im 4. Jahr sicher etwas reduzieren.</p>
<p>Wie bereits erwähnt, wird ein wesentliches Ziel auch sein, die bereits gedruckten Bücher zu verkaufen. Dies ist auch die Bedingung für 
die Herausgabe weiterer Bücher: Spätestens in einem Jahr soll VSTENO also selbsttragend sein.</a>

<h2>Danke</h2>
<p>Ganz herzlich bedanke mich bei allen, welche VSTENO im vergangenen Jahr unterstützt haben, sei es nun durch Spenden, Buchkäufe oder - 
zum Teil intensive und spannende - Rückmeldungen und Diskussionen rund um das Programm und die Stenografie ganz allgemein. Ohne diese 
