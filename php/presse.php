<p><h1>
VSTENO oder: Wenn Computer Steno lernen …</h1></p>

<p>Lektüre in Steno ist ein Bereich, der bis jetzt relativ schlecht
abgedeckt ist. Zwar gibt es die (sehr empfehlenswerten!) Lesehefte von
Silvia Seeholzer. Was aber, wenn man beispielsweise einen Roman der
Weltliteratur lesen möchte? Meines Wissens gibt es bis dato keinen
offiziellen „Verlag“ oder „Herausgeber“ stenografischer Belletristik.
</p><p>
Um diesem Mangel beizukommen, begann ich im April 2018 ein
Computerprogramm namens VSTENO zu entwickeln. Die Idee:
Langschrifttexte automatisiert in Kurzschrift zu übertragen. Das
Programm sollte also (1) Stenozeichen darstellen, (2) Übertragunsregeln
(z.B. Kürzungen, Vor-/Nachsilben etc.) anwenden und (3) das Resultat in
verschiedenen Formaten (Bildschirm, E-Reader, PDF, Druckerzeugnis) zur
Verfügung stellen. 
</p><p>
Persönlich reizte mich das Projekt aus zwei Gründen: Einerseits bin ich
Linguist (Romanist) mit einem ausgeprägten Hang zur formalisierten
(abstrakten) Betrachtung sprachlicher Phänomene. Andererseits
faszinieren mich seit den 80ern Computer (C64, Amiga). Meine
Leidenschaft bezieht sich dort vor allem auf das Programmieren und
Entwickeln von (neuen) Algorithmen. Seit vielen Jahren setze ich mich
auch als Aktivist und Mitglied der Free Software Foundation (FSF) für
Freie Software (GNU/Linux) ein. 
</p><p>
Inzwischen stecken in VSTENO rund 1300 Stunden Arbeit. Integriert
wurden vier Sprachen (Deutsch, Spanisch, Französisch, Englisch) in
Stolze-Schrey. Auf Deutsch ist neben der Grundschrift auch die
Eilschrift (teilweise) verfügbar. Mittlerweile sind die generierten
Stenogramme (aus meiner Sicht) durchaus brauchbar. Viele Wörter werden
korrekt generiert, auch wenn das Resultat ästhetisch da und dort
zweifellos noch besser sein könnte. 
</p><p>
Wichtig ist mir, dass das primäre Ziel erreicht wurde. Mit VSTENO
lassen sich nun tatsächlich ganze Romane übertragen und in Steno lesen.
Als Beispiel: Im Mai 2019 fuhren meine Frau und ich mit dem Fahrrad von
Oslo nach Bergen. Mit im Gepäck: die beiden norwegischen Autoren
Fridtjof Nansen „Mit Schneeschuhen übers Gebirge“ und Bjørnsterne
Bjørnson „Synnøve Solbacken“. Zwei eher unbekannte Werke, die ich
passend zu den Landschaften, die wir durchquerten, abends zur Erholung
gemütlich auf einem E-Reader lesen konnte. 
</p><p>
Nebst dem Generieren von „Lesestoff“ eignet sich VSTENO aus meiner
Sicht aber auch als Lerntool. Gerade wenn man sich als Neuling
erstmalig an die Schrift wagt, kann es hilfreich sein, einzelne Wörter
oder Sätze in VSTENO einzugeben (was direkt im Internet-Browser möglich
ist) und das generierte Resultat mit dem eigenen zu vergleichen. 
</p><p>
Ganz herzlich bedanken möchte ich mich beim SSV. Von Anfang an gab es
Personen, wie z.B. Sandra Bernhard und Urs Hollenstein, welche dem
Programm sehr offen und unterstützend gegenüberstanden und mich auch
motivierten weiterzumachen. Ein ganz spezieller und grosser Dank
gebührt Yvonne Reith. Sie hat unzählige mit VSTENO generierte Seiten
gegengelesen und Korrekturen und Feedbacks angebracht. Nur dank ihr
konnte letztlich das deutsche System die Qualität erreichen, die es
heute hat. 
</p><p>
Interessiert, mehr über VSTENO zu erfahren? Dann gibt es hier weitere
Infos: (1) auf der Webseite www.vsteno.ch und (2) an der
Abgeordnetenversammlung am 16. Mai 2020, zu der ich freundlicherweise
eingeladen bin, um eine kurze Live-Demo zu zeigen.
</p>

