<p>VSTENO steht unter der sogenannten GPL (GNU General Public License) und ist somit Freie Software. Das Programm darf also kopiert, verändert und 
weitergegeben werden. Man bezeichnet dies auch als Copyleft. Im Unterschied zum Copyright schützt das Copyleft die Freiheit eines Programmes.</p>
<p>Obwohl ich als Autor die Möglichkeit gehabt hätte, das Programm unter einem normalen Copyright herauszugeben (d.h. dessen Verwendung und Weitergabe einzuschränken und
für die Benutzung Lizenzgebühren zu verlangen), tue ich dies aus einer Grundüberzeugung heraus nicht:</p>
<ul>
<li>Zum einen bin ich der Meinung, dass nur Freie Software eine freie Gesellschaft ermöglicht.</li> 
<li>Zum anderen hätte ein Copyright die Weiterentwicklung des Programmes von vornherein eingeschränkt.</li>
</ul>

<p>Als Beispiel: Wenn jemand - basierend auf VSTENO - ein System für die DEK (Deutsche Einheits Kurzschrift) umsetzen möchte, so ist dies dank der GPL möglich.</p>
<h2>Gratis?</h2>
<p>Eine immer wieder diskutierte Frage ist die, ob Freie Software gratis sei? Meinerseits möchte ich hierzu als Autor von VSTENO klar Stellung beziehen:</p>
<ul>
<li><p>Als erstes gilt: Nichts auf dieser Welt ist gratis! Für alles muss eine Leistung erbracht werden, das ist wohl ein physikalischer (thermodynamischer) Grundsatz - und im übrigen der Grund,
warum es keine "Perpetuum Mobile" gibt. Freie Software kommt also nicht von nichts: Dahinter steckt immer Arbeit!</p></li>
<li><p>Als nächstes gilt: Anhänger Freier Software sind oft Idealisten. Sie glauben - entgegen jeder Vernunft - an eine bessere Welt. Insofern sind sie tatsächlich bereit, auf das klassische "do,
ut des" zu pfeifen, und einfach mal Zeit, Energie und mitunter auch Geld in etwas zu investieren, wovon sie nicht wissen, ob sie jemals etwas "zurückerhalten" werden. Viele Autoren gehen
tatsächlich soweit, ihre Programme als gratis zu deklarieren, verbunden mit der Bitte um eine Spende (was etwas widersprüchlich scheint).</p></li>
<li><p>Drittens füge ich persönlich an: Ich war nie ein Fan der Idee, dass Freie Software gratis sein soll. Als Student, das ist wahr, habe ich für Freie Software praktisch nie bezahlt (war damals
auch eine Frage des Budgets). Heute ist es für mich keine Frage, dass ich für Freie Software, die ich verwende, auch bezahle. Ich sehe das so: Würde ich kommerzielle (also unfreie) Programme
verwenden, müsste ich diese kaufen (und dabei Lizenzgebühren entrichten). Warum soll ich also für Freie Software nicht bezahlen - zumal mir diese Programme, im Unterschied zur kommerziellen Software,
zusätzliche Freiheiten gewähren?
</p></li>
<li><p>Als letztes: Diese "zusätzlichen Freiheiten" stellen einen Mehrwert der Software dar, der nicht unerheblich ist. So dürfen Sie das Programm zum Beispiel auf so vielen Geräten installieren
wie Sie möchten und sogar die "Lizenzgebühr" können Sie frei wählen (also z.B. mehr oder weniger spenden, je nachdem, ob Sie ein Programm oft oder selten verwenden oder ein Projekt mehr oder 
weniger unterstützen möchten).</p></li>
</ul>
</p>
<p><b>Fazit:</b> Dadurch, dass ich das Programm unter die GPL stelle, gewähre ich Ihnen alle erdenklichen Freiheiten. Inklusive der Freiheit, das Programm GRATIS zu verwenden - und
das sage ich ohne Ironie: Wenn Sie Student ohne Geld oder sonst in einer Situation sind, in der Sie keine Spende tätigen können (oder möchten), dann verwenden Sie dieses Programm bitte gratis 
(und ohne sich zu einer Spende verpflichtet zu fühlen).</p>
<p>Wenn Sie aber über die finanziellen Mittel verfügen, dann bitte ich Sie, kurz innezuhalten und zu überlegen, was Freie Software für unsere Gesellschaft bedeuten kann: Keine Copyright-Hölle, 
in der irgendwelche Konzerne Sie überwachen, um herauszufinden, ob Sie allenfalls "illegal" kostenpflichtige Produkte herunterladen, sondern freien Zugang
zu allen Inhalten. Eine Gesellschaft, in der Sie selber entscheiden, ob und wofür Sie bezahlen (und welche Projekte oder Firmen Sie letztlich unterstützen und welche nicht).</p>
<p>Das wäre das Schöne daran: eine Freie Gesellschaft. 
Die Kehrseite davon: Eine solche Freiheit geht mit Verantwortung einher. Niemand <u>zwingt</u> Sie dazu, "Urhebern" für ihre Arbeit etwas zu bezahlen. Dies ist im übrigen genau die
Situation, die wir heute im Internet weit gehend vorfinden: Die meisten Dienste sind "gratis" und die Konsument/innen gehen inzwischen auch davon aus (weil sie sich daran gewöhnt haben), dass 
alles "gratis" ist.<p>
</p>Aber ich bestehe noch einmal darauf: Nichts ist gratis! Wenn es nichts "kostet", dann bezahlen Sie anders für das Produkt (z.B. indem Sie endlos Werbebanner und -clips ertragen oder Konzerne
Ihre Daten zu Geld ummünzen). Deshalb noch einmal: Wenn Sie dies nicht wollen, dann treffen Sie bitte die richtige Entscheidung und spenden Sie an Freie-Software-Projekte. Es muss nicht an mich 
bzw. an VSTENO sein: Ich habe selber jahrelang von Freier Software und der Arbeit anderer profitiert, deshalb ist es für mich auch in Ordnung, wenn Sie an andere Projekte (wie z.B. die 
<a href="https://www.fsf.org/">Free Software Foundation</a> oder <a href="https://trisquel.info">Trisquel</a> - die Linux-Distribution, auf welcher VSTENO entwickelt wurde) spenden.<p>
</p>Meine Bitte ist somit nur die:<br>Tun Sie einen Schritt Richtung <u>freiwillige</u> Bezahlung der Urheber/innen - es kommt uns allen zugute!
