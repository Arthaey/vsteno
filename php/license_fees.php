<h1>Lizenzgebühren</h1>
<h2>Vorbemerkung</h2>
<p>VSTENO ist quelloffenes, öffentliches, nicht-kommerzielles Projekt, das unter verschiedenen freien Lizenzen (<a href="https://www.gnu.org/licenses/gpl-3.0">GPL</a> und <a href="https://static.fsf.org/nosvn/directory/fdl-1.3-standalone.html">FDL</a>, 
sowie <a href="https://en.wikipedia.org/wiki/MIT_License">MIT</a> für einige Teil) steht. Ziel des Projektes war es von Anfang an, nicht nur das Programm VSTENO 
selber zu entwickeln, sondern damit auch stenografische Texte herauszugeben.</p>

<p>Im Zusammenhang mit diesen Texten stellt sich zunächst die Frage 
des <a href="library_copyright.php">Urheberrechts</a>. Weiter stellt sich aber auch die Frage, ob und in welcher Form allfällige Lizengebühren 
zu entrichten sind. Auch hier regeln u.U. Lizenzen, wie z.B. die 
<a href="https://www.gutenberg.org/wiki/Gutenberg%3AThe_Project_Gutenberg_License">Project Gutenberg License</a>, ob und in welchem Umfang 
Lizenzgebühren anfallen.</p>
<p>Wie bereits erwähnt, ist VSTENO ein nicht-kommerzielles - und somit nicht-gewinnorientiertes - Projekt. Im Prinzip werden also weder für das 
Programm noch die damit erstellten Texte "Gebühren" (im Sinne von "fees" gemäss 1.E.8 der Project Gutenberg License) verlangt. Dennoch bietet 
VSTENO die Möglichkeit, dem Projekt Spenden zukommen zu lassen, mit denen in erster Linie Projekt-Kosten 
(z.B. Hosting) gedeckt werden.</p> 
<p>Es ist indes klar, dass - sobald diese Kosten gedeckt sind - ein "Überschuss" entsteht, der - je nach Berechnung und Betrachtung als 
"Gewinn" (entstanden aus "freiwilligen Beiträgen") interpretiert werden kann. Im Folgenden soll deshalb genauer geklärt werden, was unter 
diesem Überschuss zu verstehen ist und wie daraus die Lizenzgebühren entrichtet werden.</p>
<p>Grundsätzlich ist festzuhalten, dass sowohl VSTENO (mit den erwähnten Lizenzen) als auch das Projekt Gutenberg (mit seiner Philosophie 
gemeinfreier Bücher) ähnliche ideelle Ziele verfolgen und gemeinsame Grundwerte (Gemeinschaftswohl) teilen. In diesem Sinne sollen allfällige 
"Überschüsse" auch beiden Projekten zugute kommen.
<h2>Begriffe</h2>
<p>Der Klarheit halber werden folgende Begriffe definiert:<p>
<ul>
    <li><b><u>Spenden:</u></b> VSTENO verwendet nur ein Spendenkonto, was zu einer gewissen Vermischung führen kann. Obwohl zwar jede/r Spender/in die 
    Möglichkeit hat, einen Spendenvermerk anzubringen, ist u.U. nicht klar, ob eine Spende für das Programm VSTENO, für die damit 
    erstellten eBooks oder für beide bestimmt ist. Es könnte also zwischen "zweckgebundener" und 
    "nicht-zweckgebundener" Spende unterschieden werden.</li>
    <ul><br>
        <li><u>zweckgebunden:</u> Die Spende wurde im Zahlungszweck mit einem klaren Vermerk versehen, wofür die Spende gedacht ist, z.B. "VSTENO", 
    "Programm VSTENO" oder "eBooks", "Lektüren" etc.</li>
        <li><u>nicht zweckgebunden:</u> Die Spende enthält keinen (klaren) Hinweis, zu welchem Zweck sie getätigt wurde (und gilt somit für beide Projektteile).</li>
    
    </ul><br>
    <li><b><u>Kosten:</u></b> Es wird unterschieden zwischen materiellen und immateriellen Kosten: 
    <ul><br>
        <li><u>materielle Kosten:</u> Hierzu gehören das Hosting (ca. 100.- / Jahr), die Miete der Internet-Adresse (Fr. 15.- / Jahr), sowie (eventuell) 
        weitere materielle Kosten wie (eine Pauschale für) technische Hilfsmittel (Computer, Bildschirm, Drucker etc.), welche für die Erstellung 
        des Programms und die Generierung der E-Books benötigt werden.</li>
        <li><u>immaterielle Kosten:</u> Hierzu gehören alle Arbeiten, die von Projektbeteiligten und Drittpersonen für das Projekt geleistet werden. Im 
        Wesentlichen geht es hier um den Zeitaufwand, der in das Projekt (und die Generierung von E-Books) gesteckt werden.</li>
    </ul>
</ul><br>
<ul>
    <li><b><u>Anteil:</u></b> Mit Anteil ist grundsätzlich der proportionale (prozentuale) Teil vom Ganzen gemeint. Dies bezieht sich sowohl 
    auf die Zeit (Anteil in Stunden) als auch auf die Menge (z.B. Anteil in Seiten, bei eBooks). Unterschieden werden folgenden Anteile:</li><br>
    <ul>
        <li><u>Projekt VSTENO:</u> Dies umfasst alles, was zeitlich zum Progamm VSTENO und dessen Entwicklung gehört. 
        Stand April 2020: ca. 1400 Stunden.
        </li>
        <li><u>eBooks (insgesamt):</u> Alles, was zeitlich zum Projekt eBooks (= mit VSTENO generierte Stenolektüren) gehört. 
        Stand April 2021: ca. 50 Stunden.</li>
        <li><u>eBooks (spezifisch, nach Lizenz/Projekt):</u> Anteil (nach Seiten / Umfang) einer einzelnen Lizenz an der Gesamtzahl der publizierten
        Lektüren. Stand April 2020: nur eine Lizenz (Project Gutenberg), welche somit 100% ausmacht.</li>
    </ul>
</ul><br>
<ul>
    <li><b><u>Abrechnungsperiode:</u></b> Zeitraum, in welchem die Berechnung der Spendenbeiträge und der Lizenzgebühren vorgenommen wird. Da 
    VSTENO Mitte April 2018 gestartet wurde, erfolgt diese von Mitte April des Jahres bis Mitte April des folgenden Jahres jeweils rückwirkend (für das 
    verangene Jahr). Die 
    Lizenzgebührenberechnung wird erstmals im April 2021 vorgenommen.</li>
</ul>
<h2>Berechnung</h2>
<p>Anhand der definierten Begriffe wird die Berechnung folgendermassen vorgenommen:</p>
<ul>
    <li>Gesamtsumme der Spenden: Der Einfachheit halber wird nicht zwischen zweckgebundenen und nicht zweckgebundenen Spenden unterschieden, sondern
    alle Spenden zu einer Gesamtsumme addiert, die für beide Projekte - VSTENO und eBooks - gemeinsam verwendet wird.</li><br>
    <li>Anteil VSTENO: mit Stand April 2020 wurden bereits viele Arbeitsstunden für das Programm an und für sich aufgewendet. Dennoch wird auch 
    hier nicht zwischen einem Anteil VSTENO und einem Anteil eBooks unterschieden.</li>
</ul>
<p>Von dieser Gesamtsumme der Spenden werden nun die Kosten des Projektes abgezogen. Hierfür werden nur die materiellen Kosten des Projektes 
berechnet, die pauschal mit Fr. 200.- veranschlagt werden. Der daraus resultierende Betrag wird als Nettospendensumme bezeichnet.</p>
<p>Von dieser Nettospendensumme wird nun der Lizenz-Anteil berechnet:</p>
<ul>
    <li>Bruttolizenzanteil: Es wird für jede Lizenz separat berechnet, wie viele Steno-Seiten als eBooks veröffentlicht wurden. Ausgehend von der 
    Nettospendensumme entspricht der Bruttolizenzanteil dem prozentualen Anteil der Gesamtsumme aller Lizenzen.</li>
</ul>
<p>Ausgehend vom Bruttolizenzanteil wird schliesslich der Tantiemen-Anteil berechnet (z.B. 20% im Fall des Project Gutenberg).

