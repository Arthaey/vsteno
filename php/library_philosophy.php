<h2>Publikationsform</h2>
<p>Die Dokumente werden in zwei Formen veröffentlicht:</p>
<ul>
    <li>eBook (PDF): Als freie stenografische eBooks in Form eines PDFs (Text in Standard-Grösse, mit Liniierung). Die Generierung der Texte 
    ab dem Originaltext aus dem Project Gutenberg. Die eBooks dürfen nur für private (nicht-öffentliche) und nicht-kommerzielle Zwecke verwendet 
    werden.</li><br>
    <li>Printversionen: In Zukunft soll es auch kostenpflichtige Printversionen ausgewählter Werke geben. Hier müssen jedoch noch letzte
    (rechtliche und praktische) Abklärungen erfolgen. Die Publikation wird voraussichtlich entweder als eBook oder als Prinversion erfolgen.</li>
</ul>
<h2>Lektorat</h2>
<p>Die publizierten eBooks werden grundsätzlich nicht lektoriert bzw. unterscheiden wir zwischen (minimal) lektorierten Version und automatisierten
Versionen</p>
<ul>
    <li>Lektorierte Version: Wurden mindestens ein Mal überlesen und Fehler korrigiert. Die Texte sind somit weitest gehend korrekt. Ein komplette 
Fehlerfreiheit kann dennoch nicht garantiert werden: zum einen wurden allenfalls Fehler übersehen, zum anderen können bei einer (späteren) 
Neugenerierung des Textes neue Fehler dazu kommen.</li><br>
    <li>Automatisierte Versionen: Diese Texte wurden vollständig automatisiert (d.h. ohne vorgängige Korrektur) übertragen. VSTENO erreicht 
    inzwischen eine sehr hohe Korrektheit (typischerweise ca. 2 Fehler pro Steno-Seite im Schnitt). Sollten Sie Fehler finden, so melden 
    Sie diese bitte an: m.maci@gmx.ch. Sie können so aktiv dazu beitragen, dass sich die Fehlerquote von VSTENO in Zukunft weiter verringert.</li>
</ul>
<p>Da VSTENO grundsätzlich Texte vollautomatisiert überträgt und sich das Programm (und die stenografischen Modelle) ständig weiterentwickeln, 
kann eine 100% Fehlerfreiheit in elektronischen Versionen prinzipiell nicht garantiert werden.</p><p> Mit gedruckten, stenografischen eBooks 
(Printversionen) sollen aber dereinst 
auch sauber lektorierte Versionen zur Verfügung stehen, die dann - von üblichen Druckfehlern abgesehen - auch hohen Qualitätsansprüchen genügen.</p>
