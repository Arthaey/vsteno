<h1>Lektorat</h1>
<p>Alle auf VSTENO publizierten eBooks werden nach einer der folgenden Methoden lektoriert:</p> 
<ul>
    <li><p><b>Korpus-Lektorierung:</b> Hier wird ausgehend vom Originaltext mithilfe des Programmes 
    <a href="http://www.etymo.net/program/download/source/mkcor.cc">MKCOR</a> zuerst ein Wortkorpus erstellt, d.h. eine Wortliste, in der jedes 
    Wort aus dem Originaltext nur ein Mal vorkommt. Diese Liste wird anschliessend übertragen und allfällige Fehler korrigiert. Danach 
    wird der gesamte Text generiert und publiziert. Da der Wortkorpus nur zwischen einem Viertel und einem Achtel des Originaltextes ausmacht, 
    können mit dieser Methode ganze Bücher sehr schnell lektoriert werden.</p></li>
    <li><p><b>Volltext-Lektorierung:</b> Hier wird der gesamte Text übertragen und Korrektur gelesen. Diese Methode ist langsamer als die erste, da der 
    gesamte Text gelesen werden muss. Der Vorteil besteht darin, dass auch Formatierungen und Wörter, die je nach Kontext anders geschrieben 
    werden, überprüft werden können.</p></li>
</ul>
<p>Die beiden Methoden lassen sich auch kombinieren. Im Anhang der eBooks wird angegeben, nach welcher Methode die Texte lektoriert wurden. Falls 
Sie weitere Fehler in den Büchern finden, so <a href="mailto:m.maci@gmx.ch">melden</a> Sie dies doch bitte. Beachten Sie bitte, dass 
keine Garantie gegeben werden kann, dass die publizierten eBooks fehlerfrei sind.</p>

