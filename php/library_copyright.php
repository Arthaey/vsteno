<h2>Streitfälle</h2>
<p>Aufgrund unterschiedlicher Urheberrechtsregelungen in den Vereinigten Staaten können Streitfälle entstehen. Beispielsweise 
stellte das Project Gutenberg bereits vor Ablauf der Schutzfristen in Deutschland Texte von Thomas Mann (1875-1954), Heinrich Mann (1871-1950) 
und Alfred Döblin (1878-1957) zur Verfügung. Dies führte 2018 zu einem <a href="https://cand.pglaf.org/germany/index.html">Gerichtsverfahren</a>
zwischen dem Springer Verlag (Inhaber der Rechte) und dem Project Gutenberg. Das Gericht entschied, dass das Project Gutenberg die Texte zwar 
weiterhin anbieten darf (weil dies in den USA legal ist), allerdings sicherstellen muss (durch Blockierung der IP-Adressen), dass diese Texte 
Deutschland nicht heruntergeladen werden können.</p>
<p>Obwohl die Schweiz in diesem Gerichtsurteil nicht erwähnt wird, ergibt sich hier vermutlich eine ähnliche Situation, von der auch andere, im 
Project Gutenberg vorhandene Autoren betroffen sind (z.B. Robert Walser 1878-1956).</p>
