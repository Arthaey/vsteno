<h1>Urheberrecht</h1>
<p>Werke - seien es nun Programme, Bücher, Fotografien etc. - sind grundsätzlich durch ein Urheberrecht geschützt. Dieses sichert den Autor/innen 
das Recht, über die Verwendung (Publikation, Verkauf etc.) dieser Werke zu bestimmen und, je nach Verwendungszweck, dafür Lizenzgebühren 
zu verlangen. Herausgeber von Werken (z.B. Verlage, eShops) verfügen ihrerseits über Urheberrechte am Produkt (Buch, eBook) und können die 
Weiterverwendung desselben einschränken und Lizengebühren verlangen.</p>
<p>Die Verwendung eines Werkes benötigt somit gemäss Urheberrecht die Zustimmung des/r Rechteinhaber/s. Allerdings ist das Urheberrecht 
zeitlich beschränkt und erlischt nach einer Zeitspanne, die Regelschutzfrist genannt wird.</p>
<h2>Regelschutzfrist</h2>
<p>Die <a href="https://de.wikipedia.org/wiki/Regelschutzfrist">Regelschutzfrist</a> definiert den Zeitraum, in welchem ein Werk durch das 
Urheberrecht geschützt ist. Die Regelschutzfrist für Bücher betrug in der Vergangenheit 30, 50 oder 70 Jahre (je nach Epoche) und liegt heute 
bei mindestens 50 Jahren (gemäss Berner Übereinkunft), 70 Jahren (im Regelfall) bzw. 100 Jahren (in wenigen Ausnahmefällen). </p>
<p>
Für die Schweiz, Deutschland 
und die meistens Industriestaaten kann von einer Schutzfrist von 70 Jahren ausgegangen werden. Diese gilt ab dem Todeszeitpunkt des/der Autors/in 
bzw. ab dem Publikationsdatum (Verlage).</p>
<h2>eBooks</h2>
<p>Bücher, deren Urheberrecht abgelaufen ist, werden auf verschiedenen Online-Portalen gratis zum Download angeboten. Obwohl die Originalwerke 
frei von Urheberrecht sind, bedeutet dies nicht, dass es die elektronischen Versionen ebenfalls sind, da eBooks digitalisiert und herausgegeben 
werden müssen. Beide - der/die Autor/in, welcher die elektronische Version erstellt hat, und der Herausgeber, welcher das eBook aufbereitet hat 
(z.B. Layout) und vertreibt - haben sich, durch ihre Arbeit am Werke, neue Urheberrechte erworben, die ihnen wiederum gemäss einem bestimmten 
Zeitraum (Regelschutzfrist) zustehen.</p>
<p>Die Tatsache, dass ein eBook gratis zum Download angeboten wird, bedeutet somit nur, dass das Buch gratis gelesen werden kann. Jede weitere 
Verwendung (z.B. insbesondere Neuaufbereitung und die Herausgabe als Stenoversion) muss zusätzlich mit dem/der Rechteinhaber/n geklärt werden.</p>
